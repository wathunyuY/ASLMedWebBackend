package com.med.ws.beans;

public class AcadDepartmentBean {
	
	private Integer acadDepartmentId;
	private String title;
	private String descr;
	private String titleEn;
	private String descrEn;
	public Integer getAcadDepartmentId() {
		return acadDepartmentId;
	}
	public void setAcadDepartmentId(Integer acadDepartmentId) {
		this.acadDepartmentId = acadDepartmentId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescr() {
		return descr;
	}
	public void setDescr(String descr) {
		this.descr = descr;
	}
	public String getTitleEn() {
		return titleEn;
	}
	public void setTitleEn(String titleEn) {
		this.titleEn = titleEn;
	}
	public String getDescrEn() {
		return descrEn;
	}
	public void setDescrEn(String descrEn) {
		this.descrEn = descrEn;
	}
	

}
